function t(key){ return I18N[currentLang][key] || key; }

function applyI18n(){
  document.querySelectorAll('[data-i18n]').forEach(el=>{
    el.innerHTML = t(el.getAttribute('data-i18n'));
  });
  document.getElementById('pricing-currency-note').textContent = t('pricingCurrencyNote');
  document.querySelectorAll('[data-i18n-ph]').forEach(el=>{
    el.placeholder = t(el.getAttribute('data-i18n-ph'));
  });
  document.getElementById('chat-persona-label').textContent =
    currentPersona === 'general' ? t('personaGeneral') : t('dom' + currentPersona.charAt(0).toUpperCase()+currentPersona.slice(1) + 'Title');
}

function setLang(lang){
  currentLang = lang;
  document.getElementById('app').setAttribute('dir', lang === 'ar' ? 'rtl' : 'ltr');
  document.documentElement.setAttribute('lang', lang);
  document.getElementById('btn-ar').classList.toggle('active', lang==='ar');
  document.getElementById('btn-en').classList.toggle('active', lang==='en');
  applyI18n();
  renderMessages();
  renderPrices();
  if(document.getElementById('auth-overlay').classList.contains('open')){
    setAuthTab(authTab);
  }
}

